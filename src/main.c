void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	int i = 0;
	for (i=0; i<10; i++)
	{
		buffer_saida[i] = m_usuario[i];
	}
	//HAL_UART_Transmit_DMA(&huart1, &buffer_saida[0],TAM_BUFFER_MAX);
	//HAL_UART_Receive_DMA(&huart1, &m_usuario[0],TAM_BUFFER_MAX);

	//HAL_UART_Transmit_DMA(&huart1, &buffer_saida[0],10);
	HAL_UART_Transmit_DMA(&huart1, &m_usuario[0],10);
	HAL_UART_Receive_DMA(&huart1, &m_usuario[0],10);

	if (m_usuario[0] != 0 && m_usuario[0] != 0x01 && m_usuario[0] != 0x02 && m_usuario[0] != 0x03){

	}
	else{
		//tratamento de dados
		tipoModulacao[0] = m_usuario[0]; //escolha do tipo de modulacao
		psc[0] = m_usuario[1]; //1o byte PSC
		psc[1] = m_usuario[2]; //2o byte PSC
		arr[0] = m_usuario[3]; //1o byte ARR
		arr[1] = m_usuario[4]; //2o byte ARR
		multSeno[0] = m_usuario[5]; //1o byte fator multiplicador da frequencia da senoide de 60Hz
		ampSeno[0] = m_usuario[6]; //Amplitude da Senoide
		ccr[0] = m_usuario[7]; //1o byte CCR
		ccr[1] = m_usuario[8]; //2o byte CCR
		enable[0] = m_usuario[9]; //ENABLE

		TIM1->PSC = (uint32_t)((psc[0]<<8) + psc[1]);
		TIM1->ARR = (uint32_t)((arr[0]<<8) + arr[1]);

		//Duty Cycle fixo
		if (tipoModulacao[0]==0)
		{
			TIM1->CCR1 = (uint32_t)((ccr[0]<<8) + ccr[1]);
			TIM1->CCR2 = (uint32_t)((ccr[0]<<8) + ccr[1]);
			TIM1->CCR3 = (uint32_t)((ccr[0]<<8) + ccr[1]);
			TIM1->CCR4 = (uint32_t)((ccr[0]<<8) + ccr[1]);
		}

		if (enable[0] == 0) //inversor desligado
		{
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_15, GPIO_PIN_RESET);
		}

		if (enable[0] == 1) //inversor ligado
		{
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_15, GPIO_PIN_SET);
		}
	}
}
